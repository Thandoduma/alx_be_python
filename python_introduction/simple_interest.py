# Simple Interest Calculator
# Formula: I = P * R * T
# Where I = Interest, P = Principal, R = Rate, T = Time

# Define the variables with given values
principal = 1000  # Principal amount in dollars
rate = 0.05      # Annual interest rate (5% as decimal)
time = 3         # Time period in years

# Calculate simple interest using the formula I = P * R * T
interest = principal * rate * time

# Print the result in the required format
print(f"The simple interest is: {interest}")