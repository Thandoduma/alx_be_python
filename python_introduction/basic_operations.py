# Basic Arithmetic Operations
# This script performs addition, subtraction, and multiplication on two predefined numbers

# Step 1: Define the two variables with their values
number1 = 10
number2 = 5

# Step 2: Perform the arithmetic operations
addition_result = number1 + number2
subtraction_result = number1 - number2  # subtracting number2 from number1
multiplication_result = number1 * number2

# Step 3: Print the results in the required format
print(f"Addition of {number1} and {number2} is {addition_result}")
print(f"Subtraction of {number1} and {number2} is {subtraction_result}")
print(f"Multiplication of {number1} and {number2} is {multiplication_result}")